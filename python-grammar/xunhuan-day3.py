#循环的两种形式:
# 一：for ** in **,print
#二：while ** ，do,break,continue
#encoding:utf-8
names=['1','2','3']
for name in names:
    print(name)

sum=0
for x in list(range(101)):
    sum=sum+x
print(sum)

l=['a','b','c']
for name in l:
    print('hello',name,'!')

n1=255
n2=1000
print(hex(n1))
